export type TagName = keyof typeof tagNames
export function tagToItems(tag:TagName):ItemName[]{
  let items:ItemName[] = []
  tagNames[tag].values.forEach((value:ItemName|TagName)=>{
    if(isItem(value)){
      items.push(value)
    }else{
      items.push(...tagToItems(value))
    }
  })
  return items
}